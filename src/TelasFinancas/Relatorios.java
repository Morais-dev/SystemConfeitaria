    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        RelatoriosDao r = new RelatoriosDao();
        DatasRelatorio relatorio = new DatasRelatorio();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        String data1S = txtData1.getText();
        String data2S = txtData2.getText();
        if(data1S.isEmpty() || data2S.isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha as datas");
            return;
        }
        
        try{
            Date data1 = sdf.parse(data1S);
            Date data2 = sdf.parse(data2S);
            relatorio.setData1(data1);
            relatorio.setData2(data2);
        }catch(ParseException e){
            JOptionPane.showMessageDialog(null, "Data inv치lida. Use dd/MM/yyyy");
            return;
        }
        if(cbxVendas.isSelected()){
        r.gerarRelatorioVendas(relatorio);
        }else if(cbxGastos.isSelected()){
            r.gerarRelatorioGastos(relatorio);
        }else{
            JOptionPane.showMessageDialog(null, "Selecione o tipo de relat칩rio que deseja");
        }
            
            
            
    }//GEN-LAST:event_jButton1ActionPerformed

